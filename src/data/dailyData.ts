export const getSeededIndex = (seed: number, arrayLength: number, offset: number = 0) => {
  // Use a simple hash of the seed and offset to get a deterministic index
  const hash = (seed * 9301 + offset * 49297 + 233280) % 233280;
  return Math.floor((hash / 233280) * arrayLength);
};
